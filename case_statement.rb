weather = 'foggy'

case (weather)

when 'sunny'
	puts "Whip out those shades it's beach time"
when 'cloudy'
	puts "Bring your umbrella with you it might get wet!"
when 'foggy'
	puts "Can you spot the shadow man?"
when 'rainy'
	puts "So many puddles to splash, so little time"
else 
	puts "Don't know what the weather is today bud. 
	Sorry, you're on your own."
end
